a= "Selamat Pagi"

#Memotong string
print(a[0:2])
#mengetahui panjang
print(len(a))
#menghilangkan white space sebelum dan sesudah karakter string
print(a.strip())
#menghasilkan kalimat berhuruf kecil
print(a.lower())
#menghasilkan kalimat berhuruf besar
print(a.upper())
#menghasilkan kalimat berhuruf besar pada awal kata
print(a.title())
#mengganti karakter/kata daalam string
print(a.replace("Pagi", "Sore"))
#mencari posisi karakter pada string
print(a.find("la"))
#memotong string menjadi list
print(a.split())
#mengetahui apakaj semua karakter string adalah angka
print(a.isnumeric())
